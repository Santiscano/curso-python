# nativo no tiene muchos metodos matematicos sino que tiene una libreria nativa llamada math
# import math as mt #as es un alias para importar la libreria de manera mas corta y legible en el codigo
import math
print(round(1.3)) # ira al mas cercano "1.5 ira a 2"
print(abs(-77)) # valor absoluto es decir saca el signo + o -  siempre regresa positivo
print(abs(55))

print(math.ceil(1.1)) #numero superior entero mas cercano es decir 2
print(math.floor(1.9999)) # quita los decimales regresa 1
print(math.isnan(23)) # devuelve false porque si es numero
print(math.pow(10, 3)) # 10 a la potencia de 3 10*10*10
print(math.sqrt(9)) # raiz cuadrada del numero que se le pase = 9 es 3
